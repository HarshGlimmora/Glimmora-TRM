def _humanize_key(key: str) -> str:
    if key in LABELS:
        return LABELS[key]
    return key.replace("_", " ").strip().capitalize()


def _inr(value: str | int | float | Decimal) -> str:
    try:
        n = Decimal(str(value))
    except Exception:
        return str(value)
    sign = "-" if n < 0 else ""
    n = abs(n)
    integer, _, frac = f"{n:.2f}".partition(".")
    if len(integer) <= 3:
        grouped = integer
    else:
        head, tail = integer[:-3], integer[-3:]
        parts: list[str] = []
        while len(head) > 2:
            parts.insert(0, head[-2:])
            head = head[:-2]
        if head:
            parts.insert(0, head)
        grouped = ",".join(parts) + "," + tail
    return f"{sign}₹{grouped}.{frac}"


def _looks_like_amount(key: str, value: Any) -> bool:
    """Heuristic: render Decimal-shaped strings or numbers as INR for amount-y
    keys. We don't INR-format rate fractions or year strings."""
    if key in {"rate", "fy", "statute", "regime", "residency", "senior_status",
               "section_ref", "rule_id", "rule_version"}:
        return False
    if isinstance(value, (int, float, Decimal)):
        return True
    if isinstance(value, str):
        try:
            Decimal(value)
            # 4-digit integers could be years — treat plain "2024" as not money
            if value.isdigit() and 1900 <= int(value) <= 2100:
                return False
            return "." in value or len(value) > 3
        except Exception:
            return False
    return False


def _render_value(key: str, value: Any) -> str:
    if value is None:
        return "—"
    if isinstance(value, bool):
        return "Yes" if value else "No"
    if isinstance(value, list):
        # Keep arrays compact — show counts so users aren't drowning in IDs.
        if all(isinstance(x, str) for x in value):
            return f"{len(value)} transaction(s)" if key == "transactions" \
                else ", ".join(value[:5]) + (" …" if len(value) > 5 else "")
        return f"{len(value)} item(s)"
    if isinstance(value, dict):
        return f"{len(value)} field(s)"
    if _looks_like_amount(key, value):
        return _inr(value)
    return str(value)


def _label_and_amount(b: dict, i: int) -> tuple[str, Any]:
    """Pick the right label + amount columns for a breakdown row.

    Different engine steps emit different shapes:

    - Slab band:        {band, rate, amount_in_band, tax}
                        → "₹4L – ₹8L @ 5%" / amount = tax
    - Surcharge bucket: {section, flat_tax, applied_rate, surcharge, capped}
                        → "§111A (capped)" / amount = surcharge
    - Chapter VI-A:     {section, amount, cap, claimed, allowed}
                        → "§80C" / amount = allowed (or amount)
    - Income line:      {description, counterparty, label, category, amount}
                        → employer / bank name / category / amount

    Internal identifiers (txn_id etc.) are intentionally never surfaced.
    """
    keys = set(b.keys())

    # ---- Slab band ---------------------------------------------------------
    if "band" in keys and ("tax" in keys or "amount_in_band" in keys):
        rate_pct = ""
        if "rate" in keys:
            try:
                pct = Decimal(str(b["rate"])) * 100
                rate_pct = f" @ {pct.normalize():f}%".replace(".0%", "%")
            except Exception:
                rate_pct = ""
        label = f"{b['band']}{rate_pct}"
        return label, b.get("tax") if "tax" in keys else b.get("amount_in_band")

    # ---- Surcharge flat-rate bucket ---------------------------------------
    if "section" in keys and "surcharge" in keys:
        capped = bool(b.get("capped"))
        label = f"§{b['section']}" + (" (rate capped)" if capped else "")
        return label, b.get("surcharge")

    # ---- Chapter VI-A allowance ------------------------------------------
    if "section" in keys and ("allowed" in keys or "claimed" in keys):
        return f"§{b['section']}", b.get("allowed", b.get("claimed", b.get("amount")))

    # ---- Generic income line ---------------------------------------------
    label = (
        b.get("description")
        or b.get("counterparty")
        or b.get("label")
        or b.get("name")
        or b.get("category")
        or (f"§{b['section']}" if "section" in keys else None)
        or f"Item {i}"
    )
    return str(label), b.get("amount")


def _to_rows(step_input: Any) -> list[FieldRow]:
    if step_input is None:
        return []
    if isinstance(step_input, dict):
        rows: list[FieldRow] = []
        for k, v in step_input.items():
            rows.append(
                FieldRow(
                    label=_humanize_key(k),
                    value=_render_value(k, v),
                    raw=str(v) if v is not None else "",
                )
            )
        return rows
    # Non-dict input — wrap in a single row.
    return [FieldRow(label="Input", value=_render_value("", step_input), raw=str(step_input))]


