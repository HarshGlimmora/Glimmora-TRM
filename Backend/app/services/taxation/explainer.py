# Gemini batch explanation
# ---------------------------------------------------------------------------

EXPLAIN_SYSTEM = (
    "You are an Indian Income Tax helper. You receive a JSON list of "
    "calculation steps from a deterministic tax engine. For each step, write "
    "ONE short paragraph (2–4 sentences, no markdown, no bullets) explaining "
    "what the step did, what its inputs mean, and how it arrived at its "
    "result. Refer to the relevant section of the Income Tax Act if it is "
    "supplied in the step's section_ref. Use Indian rupee grouping (e.g. "
    "₹12,34,567). Never invent numbers — only restate values that appear in "
    "the step. NEVER include UUIDs, transaction IDs, internal identifiers, "
    "or hex strings in your prose; refer to sources by their employer / "
    "deductor / counterparty name (or generically as 'salary credit', "
    "'interest payment', etc.) instead. Never give tax advice. Return ONLY "
    "JSON matching the schema."
)


# Internal-only fields that must never leak into user-facing prose or
# field rows. Scrubbed from the Gemini prompt and from breakdown labels.
_INTERNAL_FIELDS = frozenset({"txn_id", "tx_id", "transaction_id", "id"})


def _strip_internal(value: Any) -> Any:
    """Recursively strip internal-only keys from a JSON-like structure."""
    if isinstance(value, dict):
        return {k: _strip_internal(v) for k, v in value.items() if k not in _INTERNAL_FIELDS}
    if isinstance(value, list):
        return [_strip_internal(v) for v in value]
    return value


def _build_explain_prompt(steps: list[dict[str, Any]], regime: str, fy: str) -> str:
    payload = {
        "fy": fy,
        "regime": regime,
        "steps": [
            {
                "step": s.get("step"),
                "op": s.get("op"),
                "section_ref": s.get("section_ref"),
                "rule_id": s.get("rule_id"),
                "rule_version": s.get("rule_version"),
                # Strip txn_ids / internal ids from input + breakdown so the
                # model can't echo UUIDs back into its prose.
                "input": _strip_internal(s.get("input")),
                "breakdown": _strip_internal(s.get("breakdown")),
                "result": s.get("result"),
                "human_explanation": s.get("human_explanation"),
            }
            for s in steps
        ],
    }
    schema = {
        "type": "object",
        "properties": {
            "explanations": {
                "type": "array",
                "items": {
                    "type": "object",
                    "properties": {
                        "step": {"type": "integer"},
                        "plain_text": {"type": "string"},
                    },
                    "required": ["step", "plain_text"],
                },
            }
        },
        "required": ["explanations"],
    }
    return (
        f"```json\n{json.dumps(payload, ensure_ascii=False)}\n```\n\n"
        "Return JSON matching this schema:\n"
        f"```json\n{json.dumps(schema)}\n```"
    )


def _gemini_explanations(
    steps: list[dict[str, Any]], *, regime: str, fy: str
) -> dict[int, str]:
    """Returns {step_number: plain_text}. Empty dict if LLM unavailable / fails."""
    extractor = get_extractor()
    if extractor.is_stub or extractor._model is None:  # noqa: SLF001
        return {}
    try:
        from vertexai.generative_models import GenerationConfig, Part  # type: ignore[import-not-found]
    except ImportError:
        return {}
    try:
        prompt = _build_explain_prompt(steps, regime=regime, fy=fy)
        resp = extractor._model.generate_content(  # noqa: SLF001
            [Part.from_text(EXPLAIN_SYSTEM), Part.from_text(prompt)],
            generation_config=GenerationConfig(
                temperature=0.0,
                top_p=1.0,
                response_mime_type="application/json",
            ),
        )
        # Reuse the extractor's text extractor for finish-reason handling.
        from app.services.extraction.gemini import _extract_text  # type: ignore[attr-defined]
        raw = _extract_text(resp)
        data = json.loads(raw)
    except Exception:
        logger.exception("Trace explainer Gemini call failed; falling back to deterministic.")
        return {}

    out: dict[int, str] = {}
    for item in data.get("explanations", []):
        try:
            out[int(item["step"])] = str(item["plain_text"]).strip()
        except (KeyError, TypeError, ValueError):
            continue
    return out


# ---------------------------------------------------------------------------
# Public entry point
# ---------------------------------------------------------------------------

def explain_trace(
    trace: dict[str, Any],
    *,
    use_llm: bool = True,
) -> list[StepExplanation]:
    """Produce explanations for every step in a calculation trace.

    `use_llm=False` forces the deterministic path (useful for tests and for
    explicitly opting out of an LLM round-trip on a hot page render).
    """
    steps = trace.get("steps") or []
    regime = str(trace.get("regime", ""))
    fy = str(trace.get("fy", ""))

    llm_map: dict[int, str] = {}
    if use_llm and steps:
        llm_map = _gemini_explanations(steps, regime=regime, fy=fy)

    out: list[StepExplanation] = []
    for s in steps:
        step_num = int(s.get("step", len(out) + 1))
        op = str(s.get("op", "")) or "(unnamed)"
        rows = _to_rows(s.get("input"))

        # Breakdown rows — append after input rows when present. Different
        # engine steps emit different breakdown shapes; pick the right
        # label + amount columns for each so the user sees the band names,
        # employer names, or section refs that actually carry meaning.
        breakdown = s.get("breakdown")
        if isinstance(breakdown, list) and breakdown:
            for i, b in enumerate(breakdown, 1):
                if isinstance(b, dict):
                    label, amt_value = _label_and_amount(b, i)
                    raw = "" if amt_value is None else str(amt_value)
                    value = _inr(amt_value) if amt_value is not None else ""
                    rows.append(FieldRow(label=label, value=value, raw=raw))
                else:
                    rows.append(FieldRow(label=f"Item {i}", value=str(b), raw=str(b)))

        # Always show the step's own result as the final row.
        rows.append(
            FieldRow(
                label="Result",
                value=_inr(s.get("result", "0")),
                raw=str(s.get("result", "0")),
            )
        )

        llm_text = llm_map.get(step_num, "")
        plain = llm_text or str(s.get("human_explanation") or f"{op} step.")
        out.append(
            StepExplanation(
                step=step_num,
                op=op,
                plain_text=plain,
                fields=rows,
                source="gemini" if llm_text else "deterministic",
            )
        )
    return out
