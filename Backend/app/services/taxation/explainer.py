"""Plain-English explanations for calculation-trace steps.

The trace's `input` and `breakdown` fields are machine-friendly JSON; the
summary UI wants prose. This service produces two things per step:

  - `fields`: a list of {label, value} rows for the input/breakdown keys,
    with friendly labels and INR-formatted amounts. Deterministic, no LLM.
  - `plain_text`: a one-paragraph explanation of what the step did and how
    it produced its result. Generated by Gemini when available; falls back
    to the step's existing `human_explanation` when LLM is in stub mode or
    a call fails.

The Gemini path is a single batch call covering every step at once — much
cheaper than per-step round trips, and keeps the explanation set internally
consistent. Output is strict JSON validated against `ExplainResponse`.
"""

from __future__ import annotations

import json
import logging
from dataclasses import dataclass
from decimal import Decimal
from typing import Any

from app.services.extraction.gemini import get_extractor


logger = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Public types
# ---------------------------------------------------------------------------

@dataclass(frozen=True)
class FieldRow:
    label: str          # human label, e.g. "Taxable income"
    value: str          # already-formatted value, e.g. "₹12,00,000.00"
    raw: str            # raw stringified value, e.g. "1200000.00"


@dataclass(frozen=True)
class StepExplanation:
    step: int
    op: str
    plain_text: str
    fields: list[FieldRow]
    source: str         # "gemini" | "deterministic"

    def to_dict(self) -> dict:
        return {
            "step": self.step,
            "op": self.op,
            "plain_text": self.plain_text,
            "fields": [
                {"label": f.label, "value": f.value, "raw": f.raw}
                for f in self.fields
            ],
            "source": self.source,
        }


